export const monthNumber = (name) => {
    var number;
    switch(name) {
        case "January":
            number = 1;
            break;
        case "February":
            number = 2;
            break;
        case "March":
            number = 3;
            break;
        case "April":
            number = 4;
            break;
        case "May":
            number = 5;
            break;
        case "June":
            number = 6;
            break;
        case "July":
            number = 7;
            break;
        case "August":
            number = 8;
            break;
        case "September":
            number = 9;
            break;
        case "October":
            number = 10;
            break;
        case "November":
            number = 11;
            break;
        case "December":
            number = 12;
            break;
    }
    return number;
}